package base.ease;

public class CubicBezierConfig {

    public float startX;
    public float startY;

    public float endX;
    public float endY;

    public CubicBezierConfig(float startX, float startY, float endX, float endY) {
        super();

	    this.startX = startX;
        this.startY = startY;

        this.endX = endX;
        this.endY = endY;
    }

}
