package base.util;

public class Point3D {

    public float x;
    public float y;
    public float z;

    public Point3D(float x, float y, float z) {
        super();

	    this.x = x;
        this.y = y;
        this.z = z;
    }

    public void set(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

}
