package base.util;

public class Rectangle2DInt {

    public int x;
    public int y;

    public int width;
    public int height;

    public Rectangle2DInt(int x, int y, int width, int height) {
        super();

	    this.x = x;
        this.y = y;

        this.width = width;
        this.height = height;
    }

}
